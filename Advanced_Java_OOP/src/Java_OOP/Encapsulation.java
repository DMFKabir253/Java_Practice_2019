package Java_OOP;

public class Encapsulation {
	
	// First we are going to create private variables
	
	private String name;  // name
	private String idNum; // idnum
	private int age;  
	
	// Now we can create getter and setter methods
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIdNum() {
		return idNum;
	}
	public void setIdNum(String idNum) {
		this.idNum = idNum;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	

}
